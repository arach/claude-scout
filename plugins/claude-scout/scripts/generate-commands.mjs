#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const PLUGIN_ROOT = path.resolve(fileURLToPath(new URL("..", import.meta.url)));
const SPEC_PATH = path.join(PLUGIN_ROOT, "commands.scout.json");
const COMMANDS_DIR = path.join(PLUGIN_ROOT, "commands");
const README_PATH = path.join(PLUGIN_ROOT, "README.md");
const README_START = "<!-- scout-commands:start -->";
const README_END = "<!-- scout-commands:end -->";

function usage() {
  console.log("Usage: node scripts/generate-commands.mjs [--check|--write]");
}

function assertString(value, field, commandName) {
  if (typeof value !== "string") {
    throw new Error(`${commandName}.${field} must be a string`);
  }
}

function assertStringArray(value, field, commandName) {
  if (!Array.isArray(value) || value.some((item) => typeof item !== "string")) {
    throw new Error(`${commandName}.${field} must be an array of strings`);
  }
}

function validateSpec(spec) {
  if (!spec || spec.version !== 1 || !Array.isArray(spec.commands)) {
    throw new Error("commands.scout.json must have version: 1 and a commands array");
  }

  const seen = new Set();
  for (const command of spec.commands) {
    const commandName = command?.name;
    assertString(commandName, "name", "command");
    if (!/^[a-z][a-z0-9-]*$/.test(commandName)) {
      throw new Error(`invalid command name: ${commandName}`);
    }
    if (seen.has(commandName)) {
      throw new Error(`duplicate command name: ${commandName}`);
    }
    seen.add(commandName);

    for (const field of ["description", "argumentHint", "usageSuffix", "readmeSummary"]) {
      assertString(command[field], field, commandName);
    }
    assertStringArray(command.postInstructions, "postInstructions", commandName);

    if (command.kind === "status") {
      if (!Array.isArray(command.statusSections) || command.statusSections.length === 0) {
        throw new Error(`${commandName}.statusSections must be a non-empty array`);
      }
      for (const section of command.statusSections) {
        assertString(section?.title, "statusSections.title", commandName);
        assertStringArray(section?.scoutArgs, "statusSections.scoutArgs", commandName);
      }
      continue;
    }

    if (command.kind != null) {
      throw new Error(`${commandName}.kind must be "status" or omitted`);
    }
    assertStringArray(command.scoutArgs, "scoutArgs", commandName);
  }

  return spec.commands;
}

async function readSpec() {
  const raw = await fs.readFile(SPEC_PATH, "utf8");
  return validateSpec(JSON.parse(raw));
}

function yamlString(value) {
  return JSON.stringify(value);
}

function renderCommandMarkdown(command) {
  return [
    "---",
    `description: ${yamlString(command.description)}`,
    `argument-hint: ${yamlString(command.argumentHint)}`,
    "disable-model-invocation: true",
    "allowed-tools: Bash(node:*)",
    "---",
    "",
    "<!-- Generated by scripts/generate-commands.mjs from commands.scout.json. Do not edit by hand. -->",
    "",
    `!\`node "\${CLAUDE_PLUGIN_ROOT}/scripts/scout-companion.mjs" ${command.name} "$ARGUMENTS"\``,
    "",
    ...command.postInstructions,
    "",
  ].join("\n");
}

function renderReadmeCommandBlock(commands) {
  return [
    README_START,
    ...commands.map(
      (command) => `- \`/scout:${command.name}\` - ${command.readmeSummary}`,
    ),
    README_END,
  ].join("\n");
}

function replaceMarkedBlock(content, replacement) {
  const start = content.indexOf(README_START);
  const end = content.indexOf(README_END);
  if (start === -1 || end === -1 || end < start) {
    throw new Error(`README.md must contain ${README_START} and ${README_END} markers`);
  }
  return `${content.slice(0, start)}${replacement}${content.slice(end + README_END.length)}`;
}

async function collectDesiredFiles(commands) {
  const files = new Map();
  for (const command of commands) {
    files.set(
      path.join(COMMANDS_DIR, `${command.name}.md`),
      renderCommandMarkdown(command),
    );
  }

  const readme = await fs.readFile(README_PATH, "utf8");
  files.set(README_PATH, replaceMarkedBlock(readme, renderReadmeCommandBlock(commands)));
  return files;
}

async function findExtraCommandFiles(desiredFiles) {
  let entries = [];
  try {
    entries = await fs.readdir(COMMANDS_DIR, { withFileTypes: true });
  } catch (error) {
    if (error.code === "ENOENT") {
      return [];
    }
    throw error;
  }

  return entries
    .filter((entry) => entry.isFile() && entry.name.endsWith(".md"))
    .map((entry) => path.join(COMMANDS_DIR, entry.name))
    .filter((filePath) => !desiredFiles.has(filePath))
    .sort();
}

async function checkFiles(desiredFiles) {
  const mismatches = [];

  for (const [filePath, desired] of desiredFiles) {
    let actual = null;
    try {
      actual = await fs.readFile(filePath, "utf8");
    } catch (error) {
      if (error.code !== "ENOENT") throw error;
    }
    if (actual !== desired) {
      mismatches.push(path.relative(PLUGIN_ROOT, filePath));
    }
  }

  for (const extra of await findExtraCommandFiles(desiredFiles)) {
    mismatches.push(path.relative(PLUGIN_ROOT, extra));
  }

  return mismatches;
}

async function writeFiles(desiredFiles) {
  await fs.mkdir(COMMANDS_DIR, { recursive: true });

  for (const extra of await findExtraCommandFiles(desiredFiles)) {
    await fs.rm(extra);
  }

  for (const [filePath, desired] of desiredFiles) {
    await fs.writeFile(filePath, desired);
  }
}

async function main() {
  const mode = process.argv[2] ?? "--write";
  if (!["--check", "--write"].includes(mode)) {
    usage();
    return 2;
  }

  const commands = await readSpec();
  const desiredFiles = await collectDesiredFiles(commands);

  if (mode === "--check") {
    const mismatches = await checkFiles(desiredFiles);
    if (mismatches.length > 0) {
      console.error("Generated command files are out of date:");
      for (const mismatch of mismatches) {
        console.error(`  ${mismatch}`);
      }
      console.error("");
      console.error("Run: node scripts/generate-commands.mjs --write");
      return 1;
    }
    console.log("Generated command files are up to date.");
    return 0;
  }

  await writeFiles(desiredFiles);
  console.log("Generated Scout command files.");
  return 0;
}

process.exitCode = await main();
